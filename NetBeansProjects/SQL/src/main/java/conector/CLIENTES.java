    void mostrarTabla(String valor) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código cliente");
        modelo.addColumn("Empresa");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Dirección");
        modelo.addColumn("Ciudad");
        modelo.addColumn("Departamento");
        modelo.addColumn("Código Postal");
        modelo.addColumn("País");
        modelo.addColumn("Empleado");
        modelo.addColumn("Límite Crédito");
        tablaclientes.setModel(modelo);

        String SQL = "";

        if (valor.equals("")) {
            SQL = "SELECT * FROM clientes";
        } else {
            SQL = "SELECT * FROM clientes WHERE " + atributo + "='" + valor + "'";
        }
        String datos[] = new String[12];

        Statement st;

        try {
            st = s.cn.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                datos[8] = rs.getString(9);
                datos[9] = rs.getString(10);
                datos[10] = rs.getString(11);
                datos[11] = rs.getString(12);
                modelo.addRow(datos);
            }
            tablaclientes.setModel(modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "error");
        }

    }
    private void REGISTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_REGISTRARActionPerformed
        try {
            // TODO add your handling code here:
            String consulta = "SELECT * FROM clientes where id_cliente = ?";
            PreparedStatement stmt = s.cn.prepareStatement(consulta);
            stmt.setString(1, IDC.getText());
            ResultSet resultados = stmt.executeQuery();

            if (resultados.next()) {
                JOptionPane.showMessageDialog(null, "This id is already related to a customer.");
            } else {
                PreparedStatement pps = s.cn.prepareStatement("INSERT INTO clientes(id_cliente,empresa,nombre,apellido,telefono,direccion,ciudad,departamento,codigoPostal,pais,id_empleado,limiteCredito) VALUES (?,?,?,?,?,?,?,?,?,?,?,?)");
                pps.setString(1, IDC.getText());
                pps.setString(2, EMPRESAC.getText());
                pps.setString(3, NOMBREC.getText());
                pps.setString(4, APELLIDOC.getText());
                pps.setString(5, TELEFONOC.getText());
                pps.setString(6, DIRECCIONC.getText());
                pps.setString(7, CIUDADC.getText());
                pps.setString(8, DPTOC.getText());
                pps.setString(9, CPC.getText());
                pps.setString(10, PAISC.getText());
                pps.setString(11, EDOC.getText());
                pps.setString(12, LCC.getText());

                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "DATOS GUARDADOS");
                limpiarCajas();
                mostrarTabla("");
                pps.close();
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "REGISTRO NO HECHO");
        }

    }//GEN-LAST:event_REGISTRARActionPerformed

