    private void realizarVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_realizarVActionPerformed
        // TODO add your handling code here:

        //ACCION PARA INGRESAR LA VENTA A LA LISTA
        boolean conf = false; //para confirmar si el producto esta en la lista
        int codV = Integer.parseInt(CodigoProductoV.getText());
        int cprod = Integer.parseInt(CodigoProductoV.getText());
        String nom = prodV.getText();
        double prec = Double.parseDouble(precioV.getText());

        //SI LA LISTA ESTÀ VACIA ENTRA AQUI SINO EJECUTA LA ACCION
        if (fechaV.getText() == "" || CodigoProductoV.getText() == "" || cantidadV.getText() == "" || efectivoV.getText() == "" || vueltoV.getText() == "" || prodV.getText() == "" || cantidadV.getText() == "" || precioV.getText() == "" || totalaPV.getText() == "") {
            JOptionPane.showMessageDialog(null, "Todos los campos deben ser rellenados para poder ingresar el producto a sistema");
        } else {

            for (int i = 0; i < a.getIngresarStock().size(); i++) {

                if (codV == a.getIngresarStock().get(i).getId()) {

                    if (a.getIngresarStock().get(i).getCantidad() == 0) { // SI LA CANTIDAD DE PRODUCTO EN LA LISTA ES IGUAL A 0 NO SE REALIZA LA VENTA
                        JOptionPane.showMessageDialog(null, "No hay existencia del producto que quiere vender, ingrese!!");
                    } else if (a.getIngresarStock().get(i).getCantidad() > 0) {
                        LocalDate fechal = LocalDate.parse(fechaV.getText());
                        int cant = Integer.parseInt(cantidadV.getText());
                        double total = Double.parseDouble(totalaPV.getText());
                        double efect = Double.parseDouble(efectivoV.getText());
                        int id = i;
                        double vuelto = Double.parseDouble(vueltoV.getText());

                        productos produc = new productos(cprod, nom, prec, cant);
                        venta ventas = new venta(produc, total, efect, cant, id, vuelto, fechal);
                        cantidadV.setText("");
                        totalaPV.setText("");
                        vueltoV.setText("");
                        efectivoV.setText("");
                        a.AgregarVentas(ventas);
                        ventas.ImprimirFactura();
                        a.getIngresarStock().get(i).Rcantidad(cant);
                        a.getIngresarStock().get(i).notificar();
                        conf = true;
                        break;

                    }

                }
                //else if (codV != a.getIngresarStock().get(i).getId()) {
                   // JOptionPane.showMessageDialog(null, "Producto no encontrado en la bases de datos de los productos");
                //}

            }
            if (conf == false) {
                JOptionPane.showMessageDialog(null, "Producto no encontrado en la bases de datos de los productos");
                cantidadV.setText("");
                totalaPV.setText("");
                vueltoV.setText("");
                efectivoV.setText("");
                prodV.setText("");
                CodigoProductoV.setText("");
                fechaV.setText("");
                precioV.setText("");
           }

        }


    }//GEN-LAST:event_realizarVActionPerformed

