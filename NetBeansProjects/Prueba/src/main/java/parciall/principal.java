    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        //ACCION PARA OBTENER GANANCIAS DE LAS INVERSIONES Y VENTAS QUE SE REALICEN
        double totalinv = 0;
        double totalvent = 0;
        double obtGanancia = 0;
        for (int i = 0; i < a.getInversiones().size(); i++) {
            totalinv += a.getInversiones().get(i).getCantidad();
            for (int j = 0; j < a.getVentas().size(); j++) {
                totalvent += a.getVentas().get(j).getTotal();
            }
        }

        JOptionPane.showMessageDialog(null, "La ganancia total obtenidas es " + a.ObtenerGanancia(totalvent, totalinv, obtGanancia));


    }//GEN-LAST:event_jButton2ActionPerformed

