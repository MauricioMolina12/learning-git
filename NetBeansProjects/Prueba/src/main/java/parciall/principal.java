    private void cantidadVKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cantidadVKeyReleased
        // TODO add your handling code here:

        //ACCION PARA CALCULAR EL TOTAL DE LA VENTA
        int cod = Integer.parseInt(CodigoProductoV.getText());
        int cant = Integer.parseInt(cantidadV.getText());
        double total = 0;
        for (int i = 0; i < a.getIngresarStock().size(); i++) {
            if (cod == a.getIngresarStock().get(i).getId()) {
                total = cant * a.getIngresarStock().get(i).getPrecio();
            }
        }

        totalaPV.setText("" + total);
    }//GEN-LAST:event_cantidadVKeyReleased

    private void efectivoVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_efectivoVActionPerformed
        // TODO add your handling code here:

        //ACCION POR SI EL EFECTIVO ES MENOR AL MONTO QUE SE DEBE PAGAR
        double tot = Double.parseDouble(totalaPV.getText());
        double efect = Double.parseDouble(efectivoV.getText());

        if (efect < tot) {
            JOptionPane.showMessageDialog(null, "el valor del efectivo es menor del total a pagar");
        }
    }//GEN-LAST:event_efectivoVActionPerformed

    private void efectivoVKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_efectivoVKeyReleased
        // TODO add your handling code here:
        //AQUI SE VA A CALCULAR EL VUELTO DE LA VENTA

        double tot = Double.parseDouble(totalaPV.getText());
        double efect = Double.parseDouble(efectivoV.getText());
        double vuelto;

        if (efect >= tot) {
            vuelto = efect - tot;
            vueltoV.setText("" + vuelto);
        }
    }//GEN-LAST:event_efectivoVKeyReleased

