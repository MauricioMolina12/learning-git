    private void reducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reducirActionPerformed
        // TODO add your handling code here:

        //ACA SE VA A REDUCIR LA CANTIDAD DE PRODUCTOS DE LA LISTA
        int cod = Integer.parseInt(codigoRP.getText());
        int cant = Integer.parseInt(cantidadRP.getText());
        boolean x = false;
        for (int i = 0; i < a.getIngresarStock().size(); i++) {
            if (cod == a.getIngresarStock().get(i).getId()) {
                a.getIngresarStock().get(i).Rcantidad(cant);
                JOptionPane.showMessageDialog(null, "Cantidad de producto reducida"
                        + "\nNombre del producto: " + a.getIngresarStock().get(i).getNombre()
                        + "\nCantidad actualizada: " + a.getIngresarStock().get(i).getCantidad());
                codigoRP.setText("");
                cantidadRP.setText("");
                a.getIngresarStock().get(i).notificar();
                x = true;
                break;
            }
        }
        if (x == false) {
            JOptionPane.showMessageDialog(null, "Producto no encontrado en la base de datos");
        }
    }//GEN-LAST:event_reducirActionPerformed

