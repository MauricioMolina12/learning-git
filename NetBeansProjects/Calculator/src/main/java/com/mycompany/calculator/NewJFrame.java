    private void IRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IRActionPerformed
        // TODO add your handling code here:

        double FIRN = Double.parseDouble(FN.getText());
        double SECN = Double.parseDouble(SN.getText());
        int x = OPC.getSelectedIndex();

        switch (x) {
            case 0:
                JOptionPane.showMessageDialog(null, "ESCOJE");
                break;
            case 1:
                JOptionPane.showMessageDialog(null, "RESULTADO: " + c.Suma(FIRN, SECN));
                break;
            case 2:
                JOptionPane.showMessageDialog(null, "RESULTADO: " + c.Resta(FIRN, SECN));
                break;
            case 3:
                JOptionPane.showMessageDialog(null, "RESULTADO: " + c.Multi(FIRN, SECN));
                break;
            case 4:
                if (SECN == 0.0) {
                    JOptionPane.showMessageDialog(null, "¡NO SE PUEDE DIVIDIR ENTRE 0!", "ERROR", HEIGHT);
                } else {
                    JOptionPane.showMessageDialog(null, "RESULTADO: " + c.Divi(FIRN, SECN));   
                }
                break;
        }
    }//GEN-LAST:event_IRActionPerformed

