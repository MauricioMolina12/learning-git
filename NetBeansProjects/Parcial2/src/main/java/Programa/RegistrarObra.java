    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int idObra = Integer.parseInt(ido.getText());
        String NombreObra = nombreobra.getText();
        String GeneroO = genero.getText();
        String DuracionObra = duracion.getText();
        double PrecioObra = Double.parseDouble(precio.getText());
        int NumeroBoletos = Integer.parseInt(Nboletos.getText());
        int Sala = Integer.parseInt(IDSALA.getText());
        boolean y = true;
        boolean z = true;
        boolean d = true;
        
        for (Sala x : RegistrarSala.S) {
            
            if (Sala == x.getIDS()) {
                
                y = false;
                for (int i = 0; i < O.size(); i++) {
                    if (idObra == O.get(i).getIdObra()) {
                        JOptionPane.showMessageDialog(null, "¡ESTA OBRA YA EXISTE!", "¡ERROR!", HEIGHT);
                        d = false;
                        break;
                        
                    }
                }
                
                if (NumeroBoletos == 0 || NumeroBoletos < 0) {
                    JOptionPane.showMessageDialog(null, "¡IMPOSIBLE QUE HAYA UNA OBRA CON CERO O MENOS DE CERO BOLETOS!", "¡ERROR!", HEIGHT);
                    
                } else if (x.disponibilidad().equalsIgnoreCase("Disponible") && NumeroBoletos <= x.getNME() && d) {
                    O.add(new Obra(idObra, NombreObra, GeneroO, DuracionObra, PrecioObra, NumeroBoletos, x));
                    JOptionPane.showMessageDialog(null, "¡REGISTRADA!");
                    x.setUtilidad();
                    break;
                    
                }
                JOptionPane.showMessageDialog(null, "¡ESTA SALA YA ESTÁ OCUPADA!", "¡ERROR!", HEIGHT);
                
                if (NumeroBoletos > x.getNME()) {
                    JOptionPane.showMessageDialog(null, "¡EL NÚMERO DE BOLETOS EXCEDE EL NÚMERO DE ESPECTAODRES DE LA SALA!", "¡ERROR!", WIDTH);
                    z = false;
                    break;
                }
            }
        }
        
        if (y) {
            JOptionPane.showMessageDialog(null, "¡NO EXISTE ESA SALA!", "¡ERROR!", HEIGHT);
        }
        
        ido.setText("");
        nombreobra.setText("");
        genero.setText("");
        duracion.setText("");
        duracion.setText("");
        precio.setText("");
        Nboletos.setText("");
        IDSALA.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

