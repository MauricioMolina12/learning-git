    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        double b = 0;
        double monto = 0;
        double temp = 0;
        boolean c = true;
        int m = 0;

        String nobra = NOMBRES.getText();

        for (Obra obra : RegistrarObra.O) {

            if (nobra.equals(obra.getNombreObra())) {

                monto = obra.getPrecioObra();

                c = false;
                break;
            }
        }
        if (!c) {
            for (Venta venta : RegistroVenta.ve) {

                if (nobra.equals(venta.getNombreObra())) {

                    temp += venta.getNumeroBoletos();
                }
            }
            MONTO.setText("\n" + nobra + " " + (temp * monto));
            MONTO.append("");
        } else {
            JOptionPane.showMessageDialog(null, "¡OBRA NO EXISTENTE!", "¡ERROR!", HEIGHT);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

