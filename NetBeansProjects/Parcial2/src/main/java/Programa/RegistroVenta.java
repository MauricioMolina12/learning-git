    private void VENDERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VENDERActionPerformed
        // TODO add your handling code here:
        int acumulador = 0;
        String NOB = NombreObra.getText();
        int numBo = Integer.parseInt(boletos.getText());
        boolean c = true;
        boolean e = true;
        boolean z = true;
        int numb = 0;

        for (Obra v : RegistrarObra.O) {
            if (NOB.equals(v.getNombreObra())) {
                if (v.getNumeroBoletos() == 0) {
                    JOptionPane.showMessageDialog(null, "¡NO QUEDAN MÁS BOLETOS PARA LA FUNCIÓN " + NOB + "!", "¡ERROR!", HEIGHT);
                    e = false;
                    break;
                }

                else if (numBo > v.getNumeroBoletos()) {
                    JOptionPane.showMessageDialog(null, "¡LA CANTIDAD DE BOLETOS EXCEDE LA BOLETERÍA REGISTRADA PARA ESTA OBRA!", "¡ERROR!", HEIGHT);
                    z = false;
                    break;
                    
                } else {
                    ve.add(new Venta(NOB, numBo));
                    JOptionPane.showMessageDialog(null, "¡VENTA REGISTRADA!");
                    v.ReducirStock(numBo);
                    c = false;
                    break;
                }
            }
        }

        if (c && e && z) {
            JOptionPane.showMessageDialog(null, "¡OBRA NO EXISTENTE!", "¡ERROR!", HEIGHT);
        }
        NombreObra.setText("");
        boletos.setText("");
    }//GEN-LAST:event_VENDERActionPerformed

