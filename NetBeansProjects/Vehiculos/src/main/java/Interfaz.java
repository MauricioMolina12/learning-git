    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        // TODO add your handling code here:

        String Mod = Modelo.getText();
        String Mar = Marca.getText();
        String Pla = NPlaca.getText();
        int NumPuer = Integer.parseInt(NPuertas.getText());
        String Col = Color.getText();
        double kilom = Double.parseDouble(KM.getText());

        V.add(new Vehiculos(Mod, Mar, Pla, NumPuer, Col, kilom));
        JOptionPane.showMessageDialog(null, "Registrado");
        con++;

        Modelo.setText("");
        Marca.setText("");
        NPlaca.setText("");
        NPuertas.setText("");
        Color.setText("");
        KM.setText("");
    }//GEN-LAST:event_RegistrarActionPerformed

    private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
        // TODO add your handling code here:

        int x = OPCION.getSelectedIndex();
        boolean v = true;
        switch (x) {

            case 0:

                JOptionPane.showMessageDialog(null, "ELIGA LA OPCIÓN");

                break;

            case 1:
                if (V.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Lista de productos vacías, por favor ingrese uno");
                } else {
                    for (int i = 0; i < V.size(); i++) {

                        JOptionPane.showMessageDialog(null, "\nVehículo N " + i + " " + "\nModelo: " + V.get(i).getModelo() + "\nMarca: " + V.get(i).getMarca() + "\nPlaca: " + V.get(i).getPlaca() + "\nNúmero de puertas: " + V.get(i).getNP() + "\nColor: " + V.get(i).getColor() + "\nKilometraje: " + V.get(i).getKilometraje());
                    }
                }
                break;
            
            case 2:
                JOptionPane.showMessageDialog(null, "Número de Vehiculos registrados: " + con);
                
                break;
        }
    }//GEN-LAST:event_OKActionPerformed

