    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int acumulador = 0;
        boolean c = true;
        int id = Integer.parseInt(IDCOND.getText());

        for (int i = 0; i < IngresarConductor.size(); i++) {
            if (IngresarConductor.get(i).getId() == id) {
                c = false;
                break;
            }
        }
        if (c == false) {
            for (int i = 0; i < transPersonas.size(); i++) {
                if (transPersonas.get(i).getIdC() == id) {
                    acumulador++;
                }
            }
            for (int i = 0; i < transMerc.size(); i++) {
                if (transMerc.get(i).getIdC() == id) {
                    acumulador++;
                }
            }
            for (int i = 0; i < transMercPeligrosa.size(); i++) {
                if (transMercPeligrosa.get(i).getIdC() == id) {
                    acumulador++;
                }
            }
        }
        JOptionPane.showMessageDialog(null, "Cantidad de transportes: " + acumulador);

       
    }//GEN-LAST:event_jButton3ActionPerformed

    public double listarSueldos(int id) {

        double sueldoExtra = 0;

        for (int i = 0; i < transPersonas.size(); i++) {

            if (id == transPersonas.get(i).getIdC()) {

                sueldoExtra += transPersonas.get(i).getSueldo();
            }
        }
        for (int i = 0; i < transMerc.size(); i++) {

            if (id == transMerc.get(i).getIdC()) {

                sueldoExtra += transMerc.get(i).getSueldo();
            }
        }
        for (int i = 0; i < transMercPeligrosa.size(); i++) {

            if (id == transMercPeligrosa.get(i).getIdC()) {

                sueldoExtra += transMercPeligrosa.get(i).getSueldo();
            }
        }

        return sueldoExtra + 1550000;
    }

