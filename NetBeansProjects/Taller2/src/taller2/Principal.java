    private void ASIGNARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ASIGNARActionPerformed
        // TODO add your handling code here:

        //variables
        int cant = Integer.parseInt(CANTIDAD.getText());
        int horas = Integer.parseInt(HORAS.getText());
        int id = Integer.parseInt(IDCON.getText());
        boolean c = true;

        for (int i = 0; i < IngresarConductor.size(); i++) {

            if (IngresarConductor.get(i).getId() == id) {
                c = false;
                if (TP.isSelected()) {

                    transPersonas.add(new Personas(cant, horas, id));
                    JOptionPane.showMessageDialog(null, "ASIGNADO");

                } else if (TM.isSelected()) {

                    transMerc.add(new Mercancia(cant, horas, id));
                    JOptionPane.showMessageDialog(null, "ASIGNADO");

                } else if (TMP.isSelected()) {

                    transMercPeligrosa.add(new MercanciaPeligrosa(cant, horas, id));
                    JOptionPane.showMessageDialog(null, "ASIGNADO");

                }
            }

        } //Termina for
        if (c) {
            JOptionPane.showMessageDialog(null, "Conductor no existente", "ERROR", HEIGHT);
        }
        IDCON.setText("");
        CANTIDAD.setText("");
        HORAS.setText("");

    }//GEN-LAST:event_ASIGNARActionPerformed

