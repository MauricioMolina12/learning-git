    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String NC = COM.getText();
        int idCA = Integer.parseInt(IDC.getText());
        String fec = FECHA.getText();
        String mod = MODELO.getText();
        int cocarro = Integer.parseInt(CODCA.getText());
        int idvendedor = Integer.parseInt(IDV.getText());
        String Nom = NOMBREVENDEDOR.getText();
        boolean ca = true;
        boolean vend = true;
        int ven = 0;

        for (int i = 0; i < c.size(); i++) {

            if (cocarro == c.get(i).getCOC()) {
                ca = false;
                
                for (int j = 0; j < v.size(); j++) {

                    if (idvendedor == v.get(i).getIdV()) {

                        vend = false;

                        ve.add(new Venta(NC, idCA, fec, mod, cocarro, idvendedor, Nom));
                        co.add(new Comprador(NC, idCA));
                        ven++;
                        c.get(i).ReducirStock(ven);
                        c.get(i).notificar();
                        c.get(i).NullE();

                    }

                }
            }
        }
        if (ca) {
            JOptionPane.showMessageDialog(null, "Carro no existente", "ERROR", HEIGHT);
        } if (vend) {
            JOptionPane.showMessageDialog(null, "VENDEDOR NO EXISTENTE", "ERROR", HEIGHT);
        }

        String col[] = {"COMPRADOR", "ID", "FECHA", "MODELO", "CÃ“DIGO"};
        String t[][] = new String[ve.size()][6];

        for (int i = 0; i < ve.size(); i++) {

            t[i][0] = ve.get(i).getComprador();
            t[i][1] = Integer.toString(ve.get(i).getId());
            t[i][2] = ve.get(i).getFecha();
            t[i][3] = ve.get(i).getModelo();
            t[i][4] = Integer.toString(ve.get(i).getIdC());
        }
        TABLAV.setModel(new DefaultTableModel(t, col));
        COM.setText("");
        IDC.setText("");
        FECHA.setText("");
        MODELO.setText("");
        CODCA.setText("");
        IDV.setText("");
        NOMBREVENDEDOR.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

